// -------------- DIVISÂO --------------
function divisao(numero1, numero2){
    return numero1 / numero2
}
let resultadoDaDivisao = divisao(20, 5)
console.log(resultadoDaDivisao)

// -------------- MULTIPLICAÇÃO --------------
